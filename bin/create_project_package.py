#!/usr/bin/env python3
"""
创建工程文件夹，将匹配报告和简历JSON文件打包到主工程文件夹内
所有输出都在 ResuMiner_Integrated 主工程文件夹中
"""

import shutil
import json
import os
from pathlib import Path
from datetime import datetime

# 配置路径 - 所有路径都在主工程文件夹内
project_root = Path('c:/Users/Lenovo/projects/ResuMiner_Integrated')
resume_file = Path('c:/Users/Lenovo/projects/my_resume.json')
output_dir = project_root / 'data' / 'output'  # 改为工程内路径

# 创建工程文件夹
project_package_dir = project_root / 'project_package'
project_package_dir.mkdir(exist_ok=True)

# 创建子目录
reports_dir = project_package_dir / 'reports'
resume_dir = project_package_dir / 'resume'
data_dir = project_package_dir / 'data'

reports_dir.mkdir(exist_ok=True)
resume_dir.mkdir(exist_ok=True)
data_dir.mkdir(exist_ok=True)

print("="*80)
print("📦 创建工程文件夹")
print("="*80)

# 1. 复制最新的匹配报告
matcher_reports = list(output_dir.glob('matcher/match_report_*.md'))
if matcher_reports:
    latest_matcher = max(matcher_reports, key=lambda p: p.stat().st_mtime)
    shutil.copy2(latest_matcher, reports_dir / latest_matcher.name)
    print(f"✅ 复制匹配报告: {latest_matcher.name}")

# 2. 复制最新的投递报告
app_reports = list(output_dir.glob('application_reports/application_report_*.md'))
if app_reports:
    latest_app = max(app_reports, key=lambda p: p.stat().st_mtime)
    shutil.copy2(latest_app, reports_dir / latest_app.name)
    print(f"✅ 复制投递报告: {latest_app.name}")

# 3. 复制简历JSON
if resume_file.exists():
    shutil.copy2(resume_file, resume_dir / 'resume.json')
    print(f"✅ 复制简历文件: resume.json")
    
    # 同时创建一个格式化的版本
    with open(resume_file, 'r', encoding='utf-8') as f:
        resume_data = json.load(f)
    
    formatted_resume = resume_dir / 'resume_formatted.json'
    with open(formatted_resume, 'w', encoding='utf-8') as f:
        json.dump(resume_data, f, ensure_ascii=False, indent=2)
    print(f"✅ 创建格式化简历: resume_formatted.json")

# 4. 复制原始匹配数据
matcher_data = list(output_dir.glob('matcher/match_results_*.json'))
if matcher_data:
    latest_data = max(matcher_data, key=lambda p: p.stat().st_mtime)
    shutil.copy2(latest_data, data_dir / 'match_results.json')
    print(f"✅ 复制匹配数据: match_results.json")

# 5. 创建工程说明文档
readme_content = f"""# ResuMiner 工程包

**生成时间**: {datetime.now().strftime('%Y年%m月%d日 %H:%M')}

## 📁 文件夹结构

```
project_package/
├── reports/          # 匹配报告
│   ├── match_report_*.md        # 详细匹配报告
│   └── application_report_*.md  # 投递策略报告
├── resume/           # 简历文件
│   ├── resume.json              # 原始简历JSON
│   └── resume_formatted.json    # 格式化简历
└── data/             # 匹配数据
    └── match_results.json       # 原始匹配结果数据
```

## 📊 数据来源

- **官网数据**: 6,724 条
  - official_jobs_raw_latest.csv: 1,971 条
  - internship_shanghai_latest.csv: 2,932 条
  - outputs/raw: 1,821 条
- **第三方平台数据**: ~20,000 条
  - 51job: 3,924 条
  - 猎聘: 16,417 条
- **合并去重后**: ~27,000 条
- **上海岗位**: 5,330 条 (硬性过滤)

## 🎯 匹配结果

- **匹配岗位数**: 100 个
- **立即投递**: 5 个
- **高优先级**: 10 个
- **推荐投递**: 15 个
- **备选考虑**: 20 个

## 👤 候选人信息

- **姓名**: 唐圣昕
- **工作年限**: 1 年
- **核心技能**: Python编程, Java编程, Go编程, Vue开发, React开发

## 🔧 使用方法

1. 查看 `reports/application_report_*.md` 获取投递策略
2. 查看 `reports/match_report_*.md` 获取详细匹配分析
3. 查看 `data/match_results.json` 获取原始匹配数据

## 📂 主工程路径

所有文件都位于: `c:/Users/Lenovo/projects/ResuMiner_Integrated/`

---
*Generated by ResuMiner*
"""

readme_file = project_package_dir / 'README.md'
with open(readme_file, 'w', encoding='utf-8') as f:
    f.write(readme_content)
print(f"✅ 创建工程说明: README.md")

# 6. 创建元数据文件
metadata = {
    'generated_at': datetime.now().isoformat(),
    'candidate_name': '唐圣昕',
    'project_root': str(project_root),
    'data_sources': {
        'official': 6724,
        'third_party': 20341,
        'total_after_dedup': 27065,
        'shanghai_only': 5330
    },
    'match_results': {
        'total_matched': 100,
        'immediate': 5,
        'high_priority': 10,
        'recommended': 15,
        'backup': 20
    },
    'files': {
        'reports': [f.name for f in reports_dir.glob('*.md')],
        'resume': [f.name for f in resume_dir.glob('*.json')],
        'data': [f.name for f in data_dir.glob('*.json')]
    }
}

metadata_file = project_package_dir / 'metadata.json'
with open(metadata_file, 'w', encoding='utf-8') as f:
    json.dump(metadata, f, ensure_ascii=False, indent=2)
print(f"✅ 创建元数据: metadata.json")

print("\n" + "="*80)
print("✅ 工程文件夹创建完成!")
print(f"📁 位置: {project_package_dir}")
print("="*80)

# 显示最终结构
print("\n📂 最终文件夹结构:")
for root, dirs, files in os.walk(project_package_dir):
    level = root.replace(str(project_package_dir), '').count(os.sep)
    indent = ' ' * 2 * level
    print(f'{indent}{Path(root).name}/')
    subindent = ' ' * 2 * (level + 1)
    for file in files:
        file_path = Path(root) / file
        size = file_path.stat().st_size
        print(f'{subindent}{file} ({size:,} bytes)')
